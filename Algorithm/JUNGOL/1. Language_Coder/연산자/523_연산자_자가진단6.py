N, M = map(int,input().split())

if N > M:
    print(f'{N} > {M} --- 1')
else:
    print(f'{N} > {M} --- 0')

if N < M:
    print(f'{N} < {M} --- 1')
else:
    print(f'{N} < {M} --- 0')

if N >= M:
    print(f'{N} >= {M} --- 1')
else:
    print(f'{N} >= {M} --- 0')

if N <= M:
    print(f'{N} <= {M} --- 1')
else:
    print(f'{N} <= {M} --- 0')




